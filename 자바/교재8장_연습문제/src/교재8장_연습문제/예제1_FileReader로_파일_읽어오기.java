package 교재8장_연습문제;

import java.io.*;

public class 예제1_FileReader로_파일_읽어오기 {

	public static void main(String[] args) {
		FileReader fin = null;
		
		try {
			fin = new FileReader("C:\\Windows\\system.ini"); //문자 입력 스트림 생성
			int c;
			while((c=fin.read())!=-1) //문자 하나씩 파일 끝까지 읽기
				System.out.print((char)c);
			fin.close();
		}
		catch(IOException e) {
			System.out.println("입출력 오류");
		}

	}

}
